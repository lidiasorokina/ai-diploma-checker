from openai import OpenAI
from dotenv import load_dotenv
import os
import re

load_dotenv()

client = OpenAI(
    api_key=os.getenv("OPENROUTER_API_KEY"),
    base_url="https://openrouter.ai/api/v1"
)


def get_latest_year(text):
    years = re.findall(r"\b(19\d{2}|20\d{2})\b", text)

    years = [
        int(year)
        for year in years
        if 1990 <= int(year) <= 2035
    ]

    if not years:
        return None

    return max(years)


def analyze_literature(text, method_text):

    latest_year = get_latest_year(text)

    if latest_year:
        year_rule = (
            f"Самый поздний год, найденный в списке литературы: {latest_year}. "
            f"Проверяй актуальность источников относительно {latest_year} года, "
            f"а не относительно 2008, 2020 или любого другого фиксированного года."
        )
    else:
        year_rule = (
            "Годы в списке литературы не найдены. "
            "Не придумывай год проверки и напиши, что актуальность невозможно "
            "оценить автоматически."
        )

    prompt = f"""
Ты — интеллектуальная система проверки курсовых и дипломных работ.

Твоя задача — проверить СПИСОК ЛИТЕРАТУРЫ.

ВАЖНОЕ ПРАВИЛО ОЦЕНКИ:
Сначала определи наличие и качество элемента.
Если элемент присутствует явно или по смыслу — не ставь "нет".
Если элемент есть, но оформлен неидеально — ставь "частично" и пиши замечание.
"Нет" ставь только если элемент действительно отсутствует.

ПРИОРИТЕТ ТРЕБОВАНИЙ:
1. Загруженная студентом методичка.
2. Методические указания направления подготовки.
3. Нормоконтроль.

Если требования противоречат друг другу, используй более специализированный документ.
Не применяй требования нормоконтроля, если методические указания устанавливают другое требование.

ВАЖНО ПРО КОЛИЧЕСТВО ИСТОЧНИКОВ:
Не придумывай минимальное количество источников.
Если в методических указаниях нет точного требования к количеству источников, не пиши, что источников недостаточно.
Если источников мало, но методичка не задаёт минимум, пиши "частично" или замечание о возможном расширении, но не "нет".
Нормоконтроль сам по себе не должен задавать минимальное количество источников, если там это явно не указано.

ВАЖНОЕ ПРАВИЛО ПО ГОДАМ:
{year_rule}

ГОСТ:
- список литературы проверяй по ГОСТ Р 7.0.100-2018
- электронные источники дополнительно проверяй по ГОСТ Р 7.0.108-2022
- не используй ГОСТ Р 7.0.5-2008 как основной стандарт для списка литературы

КАК ОЦЕНИВАТЬ:

ГОСТ:
"да" — большинство записей оформлены полно и единообразно.
"частично" — есть записи, но оформление неполное или неоднородное.
"нет" — список отсутствует или записи почти невозможно идентифицировать.

Актуальность источников:
"да" — есть современные источники или источники соответствуют теме.
"частично" — источники есть, но часть устарела.
"нет" — почти все источники явно устарели или годы невозможно определить.

Количество источников:
"да" — количество соответствует методичке или методичка не задаёт минимум.
"частично" — источников мало, но точный минимум в методичке не найден.
"нет" — только если методичка явно требует больше источников, чем есть в работе.

Качество источников:
"да" — есть учебные, научные, нормативные, официальные или профессиональные источники.
"частично" — есть сомнительные сайты, но список не состоит только из них.
"нет" — почти все источники сомнительные, нерелевантные или непроверяемые.

Научные источники:
"да" — есть статьи, монографии, учебники, диссертации, нормативные документы или официальные данные.
"частично" — научных источников мало.
"нет" — научные/официальные источники отсутствуют.

Интернет-источники:
"да" — электронные источники есть и оформлены хотя бы частично.
"частично" — электронные источники есть, но оформление неполное.
"нет" — электронных источников нет. Но это не является ошибкой, если методичка не требует их наличие.

Методические указания:
{method_text[:10000]}

Список литературы и статистика:
{text[:12000]}

Ответ строго по шаблону.

ГОСТ: да/нет/частично
Комментарий:
текст комментария

Актуальность источников: да/нет/частично
Комментарий:
текст комментария

Количество источников: да/нет/частично
Комментарий:
текст комментария

Качество источников: да/нет/частично
Комментарий:
текст комментария

Научные источники: да/нет/частично
Комментарий:
текст комментария

Интернет-источники: да/нет/частично
Комментарий:
текст комментария

Что исправить:
1.
2.
3.

Итог:
текст итога

ВАЖНО:
- не используй markdown
- не используй таблицы
- не используй HTML
- не используй символ |
- не проверяй актуальность относительно 2008 года
- не требуй минимальное количество источников, если оно не указано в методичке
- не считай отсутствие интернет-источников ошибкой, если методичка не требует их наличие
- не будь чрезмерно строгим
- после строки "Комментарий:" делай перенос строки
"""

    models = [
        "openai/gpt-oss-120b:free",
        "openrouter/free"
    ]

    for model_name in models:

        try:

            response = client.chat.completions.create(
                model=model_name,
                messages=[
                    {
                        "role": "system",
                        "content": (
                            "Ты проверяешь список литературы. "
                            "Не придумывай минимальное количество источников. "
                            "Если методичка не задаёт минимум, не ставь 'нет' по количеству. "
                            "ГОСТ Р 7.0.5-2008 не является основным стандартом проверки списка литературы."
                        )
                    },
                    {
                        "role": "user",
                        "content": prompt
                    }
                ],
                temperature=0.15,
                max_tokens=900
            )

            return response.choices[0].message.content

        except Exception as e:

            print(
                f"Ошибка модели "
                f"{model_name}: {e}"
            )

            continue

    return (
        "Ошибка: все LLM модели "
        "временно недоступны"
    )