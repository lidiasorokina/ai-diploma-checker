from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()

client = OpenAI(
    api_key=os.getenv("OPENROUTER_API_KEY"),
    base_url="https://openrouter.ai/api/v1"
)


def analyze_intro(text: str, method_text: str):

    prompt = f"""
Ты — интеллектуальная система проверки курсовых и дипломных работ.

Твоя задача — проверить ВВЕДЕНИЕ работы.

ВАЖНОЕ ПРАВИЛО ОЦЕНКИ:
Если во введении явно присутствует нужный смысловой элемент, ставь "да".
Не ставь "нет", если элемент есть, но сформулирован неидеально.
Если элемент есть, но слабый, неполный, размытый или оформлен не по методичке — ставь "частично" только там, где это разрешено шаблоном, либо "да" и указывай проблему в комментарии.
"нет" ставь только если элемент действительно отсутствует.

КАК РАСПОЗНАВАТЬ ЭЛЕМЕНТЫ:
Цель исследования есть, если во введении есть фразы или близкие по смыслу конструкции:
"цель работы", "цель исследования", "целью является", "целью данной работы является", "цель заключается", "цель состоит".

Задачи исследования есть, если есть фразы:
"задачи работы", "задачи исследования", "для достижения цели необходимо", "были поставлены следующие задачи", "необходимо решить следующие задачи", "рассмотреть", "изучить", "проанализировать", "разработать", "выявить", "определить".

Актуальность есть, если есть фразы:
"актуальность", "актуальность темы", "тема актуальна", "актуальность исследования обусловлена", "важность", "необходимость", "значимость", "проблема", "в современных условиях".

Если объект, предмет, методы, гипотеза или практическая значимость есть в тексте — учитывай это в общем соответствии, но не требуй их как обязательные элементы, если методические указания явно этого не требуют.

Методические указания:
{method_text[:10000]}

Введение работы:
{text[:8000]}

Ответ строго по шаблону.

Общее соответствие: да/нет/частично
Комментарий:
текст комментария

Цель исследования: да/нет
Комментарий:
текст комментария

Задачи исследования: да/нет
Комментарий:
текст комментария

Актуальность: да/нет
Комментарий:
текст комментария

Что исправить:
1.
2.
3.

Итог:
текст итога

ВАЖНО:
- не используй markdown
- не используй таблицы
- не используй HTML
- не используй символ |
- не придумывай отсутствующие элементы
- не будь чрезмерно строгим
- если элемент найден по ключевым словам или смыслу, ставь "да"
- замечания к качеству формулировки пиши в комментарии, а не превращай автоматически в "нет"
- после строки "Комментарий:" обязательно делай перенос строки
"""

    models = [
        "openai/gpt-oss-120b:free"
    ]

    for model_name in models:

        try:

            response = client.chat.completions.create(
                model=model_name,
                messages=[
                    {
                        "role": "system",
                        "content": (
                            "Ты проверяешь введение студенческой работы. "
                            "Твоя логика: наличие элемента важнее идеальности формулировки. "
                            "Если элемент найден, ставь 'да', а замечания пиши в комментарии."
                        )
                    },
                    {
                        "role": "user",
                        "content": prompt
                    }
                ],
                temperature=0.15,
                max_tokens=700
            )

            content = response.choices[0].message.content

            if content:
                return content

            continue

        except Exception as e:
            print(f"Ошибка модели {model_name}: {e}")
            continue

    return "Ошибка: все LLM модели временно недоступны"