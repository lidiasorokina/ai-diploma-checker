from openai import OpenAI
from dotenv import load_dotenv
import os
import re

load_dotenv()

client = OpenAI(
    api_key=os.getenv("OPENROUTER_API_KEY"),
    base_url="https://openrouter.ai/api/v1"
)


def analyze_conclusion(text, method_text):

    lower_text = text.lower()

    goal_found = any(phrase in lower_text for phrase in [
        "цель достигнута",
        "достигнута поставленная цель",
        "была достигнута поставленная цель",
        "цель работы достигнута",
        "цель исследования достигнута"
    ])

    tasks_found = any(phrase in lower_text for phrase in [
        "были реализованы все поставленные задачи",
        "поставленные задачи",
        "были решены задачи",
        "все задачи выполнены",
        "в процессе исследования были"
    ])

    conclusions_found = any(phrase in lower_text for phrase in [
        "на основе проведённого анализа",
        "на основе проведенного анализа",
        "установлено",
        "в результате",
        "проведённое исследование подтверждает",
        "проведенное исследование подтверждает",
        "можно сделать вывод",
        "таким образом"
    ])

    practical_found = any(phrase in lower_text for phrase in [
        "могут быть использованы",
        "может быть использовано",
        "практическая значимость",
        "практическая ценность",
        "внедрение",
        "применение",
        "масштабирование",
        "могут быть дополнены",
        "рекомендации"
    ])

    auto_check = f"""
Автоматическая предварительная проверка:
Выводы найдены: {"да" if conclusions_found else "нет"}
Достижение цели найдено: {"да" if goal_found else "нет"}
Связь с задачами найдена: {"да" if tasks_found else "нет"}
Практическая значимость найдена: {"да" if practical_found else "нет"}

Если автоматическая проверка показывает "да", не ставь "нет" по этому критерию.
Если формулировка слабая — ставь "частично" или "да", а замечание пиши в комментарии.
"""

    prompt = f"""
Ты — интеллектуальная система проверки курсовых и дипломных работ.

Твоя задача — проверить ЗАКЛЮЧЕНИЕ работы.

ВАЖНО:
Сначала определи наличие элемента.
Если элемент присутствует явно или по смыслу — ставь "да".
Если элемент есть, но сформулирован слабо или неполно — ставь "частично".
"Нет" ставь только если элемент действительно отсутствует.

{auto_check}

ПРИОРИТЕТ ТРЕБОВАНИЙ:
1. Загруженная студентом методичка.
2. Методические указания направления подготовки.
3. Нормоконтроль.

Методические указания:
{method_text[:10000]}

Заключение:
{text[:8000]}

Ответ строго по шаблону.

Выводы: да/нет/частично
Комментарий:
текст комментария

Достижение цели: да/нет/частично
Комментарий:
текст комментария

Связь с задачами: да/нет/частично
Комментарий:
текст комментария

Практическая значимость: да/нет/частично
Комментарий:
текст комментария

Соответствие методичке: да/нет/частично
Комментарий:
текст комментария

Что исправить:
1.
2.
3.

Итог:
текст итога

ВАЖНО:
- не используй markdown
- не используй таблицы
- не используй HTML
- не используй символ |
- не игнорируй автоматическую предварительную проверку
- если написано "была достигнута поставленная цель", это означает достижение цели: да
- если написано "были реализованы все поставленные задачи", это означает связь с задачами: да
- если есть "в результате", "установлено", "подтверждает", это означает наличие выводов
- замечания к качеству формулировки пиши в комментарии
"""

    models = [
        "openai/gpt-oss-120b:free",
        "openrouter/free"
    ]

    for model_name in models:
        try:
            response = client.chat.completions.create(
                model=model_name,
                messages=[
                    {
                        "role": "system",
                        "content": (
                            "Ты проверяешь заключение. "
                            "Если элемент найден в тексте или в автоматической проверке, "
                            "запрещено ставить 'нет'."
                        )
                    },
                    {
                        "role": "user",
                        "content": prompt
                    }
                ],
                temperature=0.1,
                max_tokens=800
            )

            return response.choices[0].message.content

        except Exception as e:
            print(f"Ошибка модели {model_name}: {e}")
            continue

    return "Ошибка: все LLM модели временно недоступны"