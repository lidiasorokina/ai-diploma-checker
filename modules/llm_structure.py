from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()

client = OpenAI(
    api_key=os.getenv("OPENROUTER_API_KEY"),
    base_url="https://openrouter.ai/api/v1"
)


def analyze_structure(text, method_text):

    prompt = f"""
Ты — интеллектуальная система нормоконтроля курсовых и дипломных работ.

Твоя задача — проверить СТРУКТУРУ документа.

ВАЖНОЕ ПРАВИЛО ОЦЕНКИ:
Сначала определи наличие разделов и логику структуры.
Если раздел присутствует явно или по смыслу — считай его найденным.
Не ставь "нет", если раздел есть, но называется немного иначе.
Если раздел есть, но название или порядок отличаются от методички — ставь "да" или "частично" и пиши замечание в комментарии.
"Нет" ставь только если обязательный раздел действительно отсутствует.
Не требуй, чтобы все разделы назывались "Глава".
Если в работе есть самостоятельные разделы без слова "Глава", это не ошибка само по себе.
Не предлагай переносить самостоятельные разделы внутрь глав, если методические указания прямо этого не требуют.
Не придумывай новую структуру работы.
Оценивай существующую структуру, а не навязывай универсальный шаблон.

ПРИОРИТЕТ ТРЕБОВАНИЙ:
1. Загруженная студентом методичка.
2. Методические указания направления подготовки.
3. Нормоконтроль.

Если требования противоречат друг другу, используй более специализированный документ.
Не применяй требования нормоконтроля, если методические указания устанавливают другое требование.

КАК РАСПОЗНАВАТЬ РАЗДЕЛЫ:

Работа может иметь главы, разделы и самостоятельные пункты.
Не все элементы обязаны называться "Глава".

Допустимые варианты:
"Глава 1", "Глава 2";
"Раздел 1", "Раздел 2";
"1.", "1.1", "2.", "2.1";
самостоятельные разделы без слова "Глава".

Введение, заключение, список литературы и приложения обычно являются самостоятельными разделами, а не главами.
Не требуй оформление вида "Глава 1. Введение" или "Глава 4. Заключение".

Пункты вроде "Цель и задачи", "Построение документа", "Оформление таблиц", "Оформление списка источников" могут быть самостоятельными пунктами, если это соответствует логике работы или методичке.
Не предлагай переносить их внутрь теоретической части без прямого требования методички.

Заключение может называться:
"Заключение", "Выводы", "Итоги исследования", "Итоговые выводы".

Список литературы может называться:
"Список литературы", "Список использованных источников",
"Библиографический список", "Источники".

Приложения не считай обязательными, если методические указания явно не требуют их наличие.

Методические указания:
{method_text[:10000]}

Структура документа:
{text[:8000]}

Ответ строго по шаблону.

Структура работы: да/нет/частично
Комментарий:
текст комментария

Обязательные разделы: да/нет/частично
Комментарий:
текст комментария

Логика структуры: да/нет/частично
Комментарий:
текст комментария

Проблемы структуры:
1.
2.
3.

Что исправить:
1.
2.
3.

Итог:
текст итога

ВАЖНО:
- после строки "Комментарий:" делай перенос строки
- не используй таблицы
- не используй markdown
- не используй HTML
- не используй символ |
- не будь чрезмерно строгим
- учитывай смысловые аналоги названий разделов
- если раздел найден, не пиши, что он отсутствует
- проблемы оформления или названия раздела пиши в комментарии, а не превращай автоматически в "нет"
"""

    models = [
        "google/gemma-3-27b-it:free",
        "meta-llama/llama-3.2-3b-instruct:free"
    ]

    for model_name in models:

        try:

            response = client.chat.completions.create(
                model=model_name,
                messages=[
                    {
                        "role": "system",
                        "content": (
                            "Ты проверяешь структуру студенческой работы. "
                            "Если раздел найден явно или по смыслу, не ставь 'нет'. "
                            "Замечания к названию или порядку пиши отдельно."
                        )
                    },
                    {
                        "role": "user",
                        "content": prompt
                    }
                ],
                temperature=0.15,
                max_tokens=700
            )

            return response.choices[0].message.content

        except Exception as e:

            print(f"Ошибка модели {model_name}: {e}")

            continue

    return "Ошибка: все LLM модели временно недоступны"